    private void lstPodaciValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPodaciValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }

        entitet = lstPodaci.getSelectedValue();
        if (entitet == null) {
            return;
        }

        // potencijalno bolje rješenje je korištenje Reflection API
        //https://docs.oracle.com/javase/tutorial/reflect/index.html
        txtNaziv.setText(entitet.getNazivMjesta());
        txtUlica.setText(entitet.getUlica());
        txtBrojKuce.setText(entitet.getBrojKuce());
        txtPostanskiBroj.setText(entitet.getPostanskiBroj());
       
    }//GEN-LAST:event_lstPodaciValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        lblPoruka.setText("");
        entitet = new Lokacija();

        postaviVrijednostiUEntitet();

        try {
            obrada.create();
            ucitajPodatke();
            ocistiPolja();
        } catch (AgencijaException e) {
            postaviPoruku(e);
        }

    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjeniActionPerformed
        entitet = lstPodaci.getSelectedValue();
        if (entitet == null) {
            return;
        }

        postaviVrijednostiUEntitet();
        try {
            obrada.update();
            ucitajPodatke();
            ocistiPolja();

        } catch (AgencijaException e) {
            postaviPoruku(e);
        }

    }//GEN-LAST:event_btnPromjeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        entitet = lstPodaci.getSelectedValue();
        if (entitet == null) {
            return;
        }

        obrada.setEntitet(entitet);

        try {
            obrada.delete();
            ucitajPodatke();
            ocistiPolja();
        } catch (AgencijaException e) {
            postaviPoruku(e);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed
    private void postaviPoruku(AgencijaException e) {

        switch (e.getKomponenta()) {
            case "nazivMjesta":
                txtNaziv.setBorder(new LineBorder(Color.RED, 2, true));
                txtNaziv.requestFocus();
                break;
            case "ulica":
                txtUlica.setBorder(new LineBorder(Color.RED, 2, true));
                txtUlica.requestFocus();
                break;
        }

        lblPoruka.setText(e.getPoruka());
        OcistiPoruku op = new OcistiPoruku();
        op.start();
    }

    private class OcistiPoruku extends Thread {

        @Override
        public void run() {
            try {
                Thread.sleep(3 * 1000);
            } catch (InterruptedException ex) {
            }
            lblPoruka.setText("");
        }

    }
