    private void ucitajPodatke() {
        DefaultListModel<Lokacija> m = new DefaultListModel<>();      
        obrada.getPodaci(txtUvjet.getText()).forEach(s -> m.addElement(s));
        lstPodaci.setModel(m);
    }

    private void ocistiPolja() {        
        for(Component c : pnlPodaci.getComponents()){
            if (c instanceof JTextField){
                ((JTextField) c).setText("");
            } 
        }
        lblPoruka.setText("");
    }

    private void postaviVrijednostiUEntitet() {

        entitet.setNazivMjesta(txtNaziv.getText());
        entitet.setPostanskiBroj(txtPostanskiBroj.getText());
        entitet.setUlica(txtUlica.getText());
        entitet.setBrojKuce(txtBrojKuce.getText());
        obrada.setEntitet(entitet);
    }
